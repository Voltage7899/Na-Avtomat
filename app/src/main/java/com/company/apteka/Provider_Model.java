package com.company.apteka;

public class Provider_Model {
    private String id,phone,lico,name;

    public Provider_Model() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getLico() {
        return lico;
    }

    public void setLico(String lico) {
        this.lico = lico;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
